#include "libpnt.h"

t_vector		ft_3_vector_cross(t_vector vec_1, t_vector vec_2)
{
	return ((t_vector){vec_1[1] * vec_2[2] - vec_1[2] * vec_2[1],
		vec_1[2] * vec_2[0] - vec_1[0] * vec_2[2],
		vec_1[0] * vec_2[1] - vec_1[1] * vec_2[0]});
}
